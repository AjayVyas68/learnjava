package loopingAndOpertator;

public class Example4 {
    public static void main(String[] args) {
        // while loop
        var i=0;
        while (i<10)
        {
            System.out.println("@"+i);
            i++;
        }
        System.out.println("the Ends");
        int j=5;
        while (j>1)
        {
            System.out.println(j);
            j--;
        }
    }
}
